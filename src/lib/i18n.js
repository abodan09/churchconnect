/**
 * Returns a translation function `t(key)` based on the current church language setting.
 * Falls back to English for missing keys.
 * @returns {(key: string) => string}
 */
export function useTranslation() {
  const ctx = useChurchSettings();
  const lang = ctx?.settings?.language || "en";
  const dict = translations[lang] || translations["en"];
  return (key) => dict[key] ?? translations["en"][key] ?? key;
}

/**
 * Get translations for a specific language (outside React context).
 */
export function getTranslations(lang = "en") {
  return translations[lang] || translations["en"];
}

export default translations;